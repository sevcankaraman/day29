package whileloopdowhileloop;

public class DoWhileLoop01 {
    public static void main(String[] args) {

        //1)While-Loop
        int i =1;
        while (i<1){
            System.out.println("sen bir while loopsun");
            i++;
        }
        //2) Do-While Loop
        int k=1;
        do{
            System.out.println("sen bir do while loopsun");
            k++;
        }while(k<1);


    }
}
